package com.crm.common;

import java.io.Serializable;

/**
 * 
 * Condition:公共条件搜索辅助类
 *
 * @author yumaochun
 * @date  2016年4月14日
 * @version  jdk1.8
 *
 */
public class Condition implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1141099653419613850L;
	
	/**
	 * 是否模糊查询，默认值为：like
	 */
	private String op;
	
	/**
	 * 条件查询的字段名称
	 */
	private  String field;
	
	/**
	 * 条件字段名称对应的值
	 */
	private String value;
	
	/**
	 * 字段对应的类型：text等
	 */
	private String type;

	public String getOp() {
		return op;
	}

	public void setOp(String op) {
		this.op = op;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	

}
